////////////////////////////////////////////////////////////////////////////////
#include "Room.h"
#include "MoveCommand.h"
#include <cstdlib>
////////////////////////////////////////////////////////////////////////////////
Room::Room()
{
  rooms[0] = NULL;
  rooms[1] = NULL;
  rooms[2] = NULL;
  rooms[3] = NULL;
}
////////////////////////////////////////////////////////////////////////////////
Room::~Room()
{
  
}
////////////////////////////////////////////////////////////////////////////////
void Room::SetNextRoom( Direction d, Room * pRoom )
{
  rooms[d] = pRoom;
}
////////////////////////////////////////////////////////////////////////////////
Room * Room::GetNextRoom( Direction d )
{
  return rooms[d];
}
////////////////////////////////////////////////////////////////////////////////
void Room::SetDescription( const string & desc )
{
  description = desc;
}
////////////////////////////////////////////////////////////////////////////////
const string & Room::GetDescription() const
{
  return description;
}
////////////////////////////////////////////////////////////////////////////////
Room * Room::OnMoveCommand( MoveCommand *pCommand )
{
  return GetNextRoom( pCommand->GetDirection() );
}
////////////////////////////////////////////////////////////////////////////////
void Room::SetGame( Game * pGame )
{
  game = pGame;
}
////////////////////////////////////////////////////////////////////////////////
Game * Room::GetGame()
{
  return game;
}